namespace worldWizards.core.entity.utils
{
    public class IntVector3
    {
        private int x { get; set; }
        private int y { get; set; }
        private int z { get; set; }

        public IntVector3(int x, int y, int z) {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
